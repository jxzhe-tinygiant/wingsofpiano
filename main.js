function switchLang(newLang) {
    i18n.locale = newLang
    localStorage.setItem('locale', newLang)
}

document.querySelector('#zh').addEventListener('click', function() {
    switchLang('zh');
})
document.querySelector('#en').addEventListener('click', function() {
    switchLang('en');
})

var navbar = document.querySelector('.navbar')
var navbarCollapsed = true;

function updateNav() {
    var scrollTop = document.scrollingElement.scrollTop;
    if (scrollTop < 200) {
        navbar.style.backgroundColor = "rgba(33, 37, 41, " + (scrollTop / 266.67 + 0.25) + ")";
    } else {
        navbar.style.backgroundColor = "rgba(33, 37, 41, 1)";
    }
}

const bsCollapse = new bootstrap.Collapse(document.getElementById('navbarSupportedContent'), {
    toggle: false
});

function toggleNavbar() {
    if (navbarCollapsed) {
        navbar.style.backgroundColor = "rgba(33, 37, 41, 1)";
    } else {
        updateNav();
    }
}
document.querySelectorAll('.navbar-nav a').forEach(function(e) {
    e.addEventListener('click', function() {
        bsCollapse.hide();
        navbarCollapsed = true;
    })
});
document.querySelectorAll('.nav-item a.dropdown-item').forEach(function(e) {
    e.addEventListener('click', function() {
        bsCollapse.hide();
        navbarCollapsed = true;
    })
});
document.querySelector('.navbar-brand').addEventListener('click', function() {
    bsCollapse.hide();
    navbarCollapsed = true;
})
document.querySelector('.navbar-toggler').addEventListener('click', function() {
    toggleNavbar();
    navbarCollapsed = !navbarCollapsed;
})

window.addEventListener('load', function() {
    updateNav();
    document.querySelector('#loading1').style.opacity = 0;
    document.querySelector('#loading2').style.opacity = 0;
    setTimeout(function() {
        document.querySelector('#loading1').style.zIndex = -1;
        document.querySelector('#loading2').style.zIndex = -1;
    }, 500);
})

document.querySelector('body').onscroll = function() {
    var scrollTop = document.scrollingElement.scrollTop;
    document.querySelector('#bg').style.backgroundPositionY = "-" + scrollTop * 0.25 + "px";
    if (navbarCollapsed) {
        updateNav();
    }
}